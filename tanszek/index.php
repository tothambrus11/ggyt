		<a name="szakcsoport-oktato-munkaja"></a>
		<h2>A Tanszék/Szakcsoport oktató munkája</h2>
		<p>
			A Tanszéken az oktatás személyi és tárgyi feltételeinek fejlesztésénél mindig is alapvető cél volt, hogy az üzemmérnökképzés jól felkészült, korszerű ismeretekkel rendelkező gyártástechnológusok képzésére irányuljon. Ennek érdekében a Tanszék fejlesztését az ipar igényei és a felügyeleti szervek támogatása határozta meg. A szaktárgyak oktatása mellett rendszeressé vált a szakirányú tanfolyamok szervezése, 1986-tól pedig a végzett üzemmérnökök szakirányú továbbképzéséről (végül pedig a szaküzemmérnöki oklevél megszerzéséről) is gondoskodtunk.
		</p>
		<p>
			A Gépgyártástechnológiai Tanszék/Szakcsoport a Bánki Kar valamennyi szakán, ágazatán és tagozatán végez oktatási tevékenységet. 1985-ig Tanszékünk a kihelyezett tagozati képzésben (Kaposvár, Miskolc, Székesfehérvár), illetve folyamatosan a jelentős hallgatói létszámú esti-, majd levelező tagozatos képzésben is részt vesz. Az oktatott tantárgyak keretében a hallgatók megismerkedtek az ipari termelésben és az üzemi gyakorlatban alkalmazott megmunkálási módokkal, a korszerű technológiai eljárásokkal, a szerszámokkal, a készülékekkel, a gépipari méretellenőrzés módszereivel és eszközeivel. Foglakoztak továbbá a gépek, berendezések, műszerek működési elveivel, szerkezeti felépítésükkel, valamint a gyártástechnológiai folyamatokban való felhasználásukkal.
		</p>
		<p>
			A Tanszék személyi állománya mindig megfelelt mindazon elméleti és gyakorlati feltételeknek - képzettség, összetétel, üzemi és felsőoktatási gyakorlat, tudományos munka -, amelyek az oktatási-nevelési feladatok kiemelkedő színvonalú ellátásához szükségesek. Az oktatók szakmai (üzemi) gyakorlata és tapasztalatai nemcsak a mérnökképzésben rendkívül fontosak, hanem a kutatási feladatok elvárt színvonalú megoldásában is.<br>

			Az oktatómunka egyik legfontosabb tartalmi eleme tananyagaink fejlesztése, modernizálása. Ez a tevékenység nem kizárólag a tantervek időszakos felülvizsgálatával kapcsolatos, hanem olyan folyamatosan végzendő, kiemelkedő jelentőségű feladat, amely a tantárgyprogramok időnként esedékes felülvizsgálatát és aktualizálását követeli meg. Ezzel függ össze az is, hogy tantárgyainkhoz viszonylag kevés a rendelkezésre álló korszerű, nyomtatott, saját kiadású jegyzet. Nehezíti helyzetünket az is, hogy a tantárgyainkra vonatkozó szabványok, előírások és jelölések gyakran változnak. A jegyzeteket átmeneti ideig rövid, tömör útmutatók, segédletek és előadásvázlatok, elektronikus tananyagok (ppt, pdf) pótoljuk. Fontos feladatunk, hogy az új tantervekben szereplő tárgyainkhoz mielőbb elkészüljenek a szükséges nyomtatott vagy elektronikus jegyzetek, segédletek, útmutatók.

			<ul>
				<li>Oktató munkánkban egyre szélesebb körben mutatjuk be és használjuk fel az ipari gyakorlat legkorszerűbb eredményeit, tapasztalatait és módszereit;</li>
				<li>az oktatás hatékonyságának növelésére a legkorszerűbb audiovizuális eszközöket, számítógépes módszereket és demonstrációs programokat használjuk fel;</li>
				<li>a hallgatói foglalkozások jelentős hányadánál biztosítjuk a hallgatók önálló gyakorlati munkavégzését. Felügyelet mellett, de önállóan végzik a különböző megmunkálásokat, a mérések összeállítását, feldolgozását és kiértékelését;</li>
				<li>1984 óta folyamatosan fejlődött a tanszék számítógépekkel való ellátottsága. A tanszék ma három korszerű számítógépes laboratóriummal szolgálja az oktatást, kutatást.</li>
			</ul>
		</p>
		<p>
			1997-ben a Tanszék - az ipar elvárásaival és a hallgatói érdeklődés módosulásával összhangban - kidolgozta a gépgyártástechnológia szakirányt felváltó CAD/CAM szakirány képzési célkitűzéseit és oktatási programját. Ennek keretében olyan gépészmérnökök képzését végezzük, akik <i>"képesek lesznek bekapcsolódni napjaink számítógépes módszereken alapuló mérnöki munkájába, megismerik és eredményesen használják fel annak rohamosan bővülő eszközrendszerét; olyan szakemberek képzésre törekszünk, akik egyaránt jártasak a technológiai eljárások kiválasztásában és megtervezésében is." A szakirányon folyó képzés jártasságot nyújt az alakító- és forgácsolótechnológiai eljárások, a korszerű gyártóberendezések és gyártóeszközök számítógéppel segített kiválasztásában, tervezésében, valamint a különböző integráltsági szintű gyártórendszerek üzemeltetésében és felügyeletében. A termelési folyamatra vonatkozó ismeretek révén hallgatóink alkalmassá válnak a vállalati minőségügy feladatainak megoldására, a gépipari termékek minőségének tervezésére és fejlesztésére, továbbá a minőségbiztosítás területén alkalmazott újszerű módszerek és technikák felhasználására."</i>
		</p>
		<p>
			A Gépgyártástechnológiai Tanszék a gépészmérnöki szakon korábban a gépgyártástechnológia, jelenleg pedig a <b>CAD/CAM/CNC</b> szakirány gondozója (kibocsátó tanszéke), de részt vesz a gépészmérnöki szakon valamennyi szakirány, illetve valamennyi (tehát a mechatronikai mérnöki, biztonságtechnikai mérnöki, a műszaki menedzser és a mérnöktanár) szak képzésében is. A Tanszék mai, szerteágazó oktatási feladatait, tantárgyait és a más tanszékekkel közösen végzett képzés területeit a 2. sz. táblázat mutatja be.
		</p>
		<p>
			A Tanszék jelentős energiát fordít az üzemi gyakorlatok megszervezésére. Sajnos azok az ipari vállalatok, amelyek régebben jelentős létszámban fogadták hallgatóinkat, vagy megszűntek/átalakultak, vagy pedig fogadókészségük jelentősen lecsökkent. Jóllehet a hallgatók egyéni elhelyezkedésének lehetővé tétele enyhíti gondjainkat, azonban az üzemi gyakorlatok szakmai hatékonysága sok esetben nem elégíti ki elvárásainkat, és nem hasonlítható a Tanszék által korábban szervezett, irányított és ellenőrzött gyakorlatok eredményességéhez és kihasználtságához.
		</p>
		<br>
		<p>
			A Tanszék elsősorban a CAD/CAM/CNC szakirány hallgatóinak ad ki <b>szakdolgozati témákat <i>(2012/13. tanévben nappali tagozaton 56 főnek, levelező tagozaton 41 főnek)</i></b>, de évente 6 - 10 fő más szakos/szakirányos hallgató is Tanszékünk konzulenseivel készíti el szakdolgozatát. A vállalatoktól, cégektől beérkező témák kiírásakor olyan gyakorlati feladatok megfogalmazására törekszünk, amelyek kidolgozásuk után hasznosulnak is. A beadott szakdolgozatok elbírálását - üzemi szakemberek bevonásával - szintén tanszékünk szervezi. Napjainkban különböző cégek, egyesületek, alapítványok pályázatok kiírásával támogatják az új és/vagy azonnal hasznosítható eredményeket, módszereket tartalmazó szakdolgozatok készítését. Tanszékünk évről-évre 3-6 dolgozattal vesz részt a pályázaton, ahol hallgatóink eredményesen szerepelnek.
		</p>
		<p>
			A tudományos diákköri tevékenység támogatását Tanszékünk a színvonalas mérnökképzés érdekében kifejtett, nagyon fontos oktató-nevelő tevékenységnek tekinti. A <b>TDK-munka</b> személyi és tárgyi feltételei adottak, ezért az érdeklődő, legjobban felkészült hallgatóink tanszéki konzulenseik támogatásával bővíthetik szakmai ismereteiket, tapasztalataikat. E tevékenység elsődleges célja a tanulmányokat meghaladó ismeretek megszerzése és elmélyítése, a problémafeltáró- és –megoldó készség kialakítása és fejlesztése, a kísérletek megtervezésének, számítógépes kiértékelésének és elemzésének elsajátítása. Az évente sorra kerülő házi TDK-konferenciákon általában 5 - 8, a tanszék által felkészített hallgató mutatja be munkáját. A kétévenként sorra kerülő országos konferenciák műszaki szekcióiban Tanszékünket átlagosan 3 - 4 hallgató képviselte, s a magas színvonalú versengésben jó eredményeket értek el, alkalmanként 2 - 3 díjat szereztek.
