		<a name="gepgyartastechnologiai-intezet"></a>
		<h2>Gépgyártástechnológiai Intézet (1987-1990)</h2>

		<p>
			A Tanszék életében jelentős változást hozott, amikor a korábban önálló oktatási egységek összevonásával 1987. július 1-jével a főiskolán négy oktatási intézet alakult meg. A Gépgyártástechnológiai Intézet a Gépgyártástechnológiai Tanszék és a Mechanikai Technológiai Tanszék személyi állományának, eszközeinek, helyiségeinek és laboratóriumainak összevonásával szerveződött, szervezeti és működési rendjét pedig az intézet Szervezeti és Működési Szabályzata rögzítette. Az intézetnek, mint önálló szervezeti egységnek elsődleges feladata a hallgatók oktatása, nevelése, a közép- és felsőfokú szakemberek továbbképzése, valamint tudományos kutatómunka végzése volt. Az intézet vállalatokkal, esetenként pedig - a határterületi, illetve a továbbképzési feladatok megoldásában - más intézményekkel is együttműködött.
		</p>
		<p>
			Az intézet oktató-nevelő és tudományos kutató-fejlesztő munkáját, illetve az ezekkel kapcsolatos szervezési és gazdálkodási feladatait nem önálló szervezeti egységekre (szakcsoportokra) tagozódva végezte, együttműködve a Főiskola többi intézeteivel és szervezeti egységeivel. Az intézet nem önálló egységei (és a hozzájuk rendelt műhelyek és laborok) az alábbiak voltak:
			<ul>
				<li>Fémtani szakcsoport (Metallográfiai, Anyagvizsgáló, Fárasztó-, Hőkezelő- és Hegesztő laboratórium),</li>
				<li>Forgácsnélküli alakítási szakcsoport (Méréselőkészítő, Képlékenyalakítási, Műanyag és CAD számítógép-laboratórium, Szerszámkészítő műhely),</li>
				<li>Szerszámgépek szakcsoport (Szerszámgép- és CAMAC-Laboratórium),</li>
				<li>Gyártóeszközök szakcsoport (Méréstechnikai, Készülék-, Forgácsolás és szerszámai, valamint Érdességmérési és számítógépes laboratórium),</li>
				<li>Forgácsolástechnológiai szakcsoport (Szerszámgépműhely, Szereléstechnológia-laboratórium),</li>
				<li>Intézeti titkárság.</li>
			</ul>
		</p>
		<p>
			Az Intézetet az Intézeti Tanács, illetve az <i>Intézet igazgatója (Dr. Angyal Béla főiskolai tanár) irányította, az igazgató munkáját az oktatási (Dr. Váradi András főiskolai docens) és a tudományos (Dr. Czinege Imre főiskolai docens) igazgatóhelyettesek segítették.</i> A szervezeti átalakítással egy időben a tantervek korszerűsítésére is sor került. A módosított tanterv a gyártástechnológia szak képzési céljának újrafogalmazására épült, és - az 1983. évi tanterv-korszerűsítési munka eredményeire alapozva - figyelembe vette a Főiskola 2000-ig szóló fejlesztési tervének célkitűzéseit is. A gyártástechnológia szakon belül új ágazatok (Gyártóeszköz-szerkesztő, NC-technológus, Gyártásszervező, Hőkezelő-hegesztő) képzési céljait és tanterveit dolgoztuk ki. Az ágazati tanterv tantárgyai között számos olyan volt, amely a korábbi tárgyak korszerűsítését igényelte, más tantárgyak (pl. Automatizált gyártás) pedig ekkor kerültek be a főiskolai képzésbe.
		</p>
		<p>
			Az intézet oktatási és kutatási feladatainak jobb ellátására középtávú fejlesztési tervet is készített, amelyben kiemelt szerepet kapott a számítógéppel segített, irányított és integrált gyártás, a robottechnika, a számítógéppel irányított és kiértékelt mérés, a gyártóeszköz-minősítő vizsgálatok stb. A célkitűzések megvalósítása a laboratóriumok és az eszközpark korszerűsítésével járt együtt, elsősorban a korszerű számítógépes alkalmazások területén. A kutatómunka feltételeinek javulása az intézet dolgozói számára lehetővé tette, hogy részt vegyenek a nagyobb kutatási-kísérleti igényű vállalati fejlesztési feladatok megoldásában és az országos szintű, kiemelt kutatási célkitűzések megvalósításában.
		</p>
		<p>
			<i>A Főiskolán kialakított intézeti rendszer 1990-ben úgy szűnt meg, hogy helyét ismét a tanszéki rendszer foglalta el. Ez a szervezeti módosítás ismét új szakok indításával, teljesen új tantervek kidolgozásával, illetőleg a meglévők jelentős korszerűsítésével járt együtt. Az új képzési irányok megjelenésével a tanszék feladatai is módosultak, átértékelődtek, összességében azonban minden szakon a képzési célnak megfelelő arányt mutatták, a gyártástechnológia fontosságának megtartása mellett.</i>
		</p>


		<a name="gepgyartastechnologiai-tanszek"></a>
		<h2>Gépgyártástechnológiai Tanszék (1990-1999)</h2>
		<p>
			Az 1990 utáni megtorpanást a nappali tagozatos hallgatók létszámának fokozatos növekedése követte, s 1995-re a hallgatói létszám közel megduplázódott az 1990. évi létszámhoz képest. Az oktatói állomány létszáma viszont nem emelkedett, ezért oktatóinkra egyre növekvő óraterhelés jutott. A kilencvenes évektől kezdve előbb a műszaki informatikus, majd a műszaki menedzser képzés beindítása jelentett új és újabb feladatokat. A Tanszéknek kiemelkedő szerepe volt a műszaki informatikus képzés beindításában, mivel a képzés akkreditálása az Intelligens gyártócella szakirányon történt meg.<br>
			<img src="img/image008.jpg">
		</p>
		
		<p>
			Az 1996-os tantervreformot követően komoly szakmai kihívást jelentett a posztgraduális képzések kereteiben kidolgozott <b><i>Termékfejlesztő és Minőségügyi szakirányú továbbképzési szak képesítési követelményei, tantervei.</i></b> A képzések sikeresen beindultak, és a szakok hallgatóinak oktatását - jelentős óraszámban - Tanszékünk dolgozói látták el.
		</p>
