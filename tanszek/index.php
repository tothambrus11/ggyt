			Erre az időszakra esett az AIFSZ, Akkreditált Iskolarendszerű Felsőfokú Szakemberképzés beindítása is. A gépipari mérnökasszisztensek képzéséhez szintén jelentős műhelyi, laboratóriumi és tantermi óraszámú oktatással járultunk hozzá.<br>
