				<li>A különféle rendeltetésű, korszerű konstrukciójú, geometriájú, kiképzésű, élpreparációjú, anyagú és bevonatú szerszámok teljesítőképességnek komplex vizsgálata. A szerszámok minősítése a forgácsolóképesség legfontosabb jellemzőinek műszeres, on-line mérésével történik, számos esetben kísérlettervezéssel támogatva. Különösen fontos területet jelent a forgácsmanipuláció jelenségeinek feltárása, az élminőségjavító eljárásokkal elérhető tartósságnövekedés, a legújabb (döntően nanokompozit-alapú) PVD-bevonatrendszerek hatékonyságának műszaki-gazdasági elemzése, valamint e szerszámokkal kialakított felületek mikrogeometriája, pontossága és működési tulajdonságai.</li>
				<li>A környezetbarát megmunkálás (Green Manufacturing) körülményeinek vizsgálata, eredményességének számszerűsítése és fokozatos elterjesztése. A birtokunkban lévő minimálkenő berendezés (HK250), valamint a hideglevegős készülék (Cold Air Gun, CAG) váltott vagy együttes alkalmazása – különösen a XXI. század technológiai kihívásai szempontjából – egyre több területen jelenhet igazi megoldást az ipar számára.</li>
				<li>Különleges mozgásciklusú, jellemzően epiciklois szerszámpályák számítógépes programmal történő előállítása mély hornyok marására. A trochoidális marás szóba jöhető alváltozatainak kutatása nehezen megmunkálható anyagoknál a fellépő erőhatások, a pontosság és a szerszámelhasználódás szempontjából.</li>
			</ul>
		</p>
		<p>
			A fenti kutatásokban komoly szerepet játszanak a különböző kutatási pályázatok (főként az OTKA) támogatásával beszerzett mérő- és regisztráló eszközök. Például 2002-ben az OTKA M042000 témaszámú, „Forgácsolóerő- és nyomatékmérő műszer szerszámbefogóval, többcsatornás erősítővel” műszerbeszerzés történt, amelyet forgószerszámok (fúrók, menetformázók és menetfúrók, különböző rendeltetésű és konstrukciójú marók) vizsgálatához használunk fel. A kutatási munkát nagyban segíti két, elsősorban szerszámminősítésekre használt CNC-megmunkálógép (álló szerszámoknál a Dugard Eagle eszterga, forgószerszámos eljárásoknál pedig a Mazak Nexus megmunkálóközpont).
