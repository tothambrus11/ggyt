		<a name="torteneti-visszatekintes"></a>
		<h2>Történeti visszatekintés</h2>
		<p>
			A Tanszék az 1962-ben kialakított Felsőfokú Gépipari Technikum képzésszervezésének kezdeti időszakában, az 1963/64 tanévben létesült. Már 1965-től kiemelkedő szerepe volt a gyártástechnológiai szak szaktárgyi tananyagainak kidolgozásában és a főiskolai oktatás szakmai követelményeinek gyors ütemű előkészítésében. A tanszék személyi és technikai fejlesztésének irányát a gépiparnak a gyártástechnológiai üzemmérnökökkel szemben támasztott igényei szabták meg.
		</p>
		<p>
			Az intézmény életében jelentős változást hozott az 1969-es év, amikor a felsőfokú technikumot főiskolává alakították át, létrejött a Bánki Donát Gépipari Műszaki Főiskola. Az ipari fejlődés igényei a szakemberképzés területén - az egyetemeken képzett mérnökök mellett - szükségessé tették olyan üzemmérnökök képzését is, akik a technológiai eljárások, a kivitelezési ismeretek birtokában és a gyakorlati tapasztalatok alapján képesek a tervezői elgondolások megvalósítását célzó munka elvégzésére, irányítására. A szakok és ágazatok szervezése az akkori ipari igények alapján történt, és ennek megfelelően három (gépgyártástechnológiai, általános gépész, szervező) szak alakult. Ezek közül a gépgyártástechnológiai szak két ágazattal - a forgácsoló és a forgácsnélküli alakító - kezdte meg oktatási-nevelési és kutatási tevékenységét.
		</p>
		<p>
			A gépgyártástechnológia szak, képzési feladatát a következőképpen fogalmazták meg: <i>"A gépgyártástechnológus üzemmérnököknek jártasaknak kell lenniük a gazdaságos gyártási folyamat megtervezésében. Ismerniük kell a korszerű technológiai eljárásokat, a megmunkálógépeket, a szerszámokat, a gyártást elősegítő készülékeket, valamint a minőségellenőrzés korszerű eszközeit és módszereit. Feladataik ellátása érdekében megismerkednek a forgácsolás elméletével, szerszámaival, gépeivel, készülékeivel és technológiájával, a forgácsnélküli megmunkálásokkal, a hőkezelés és hegesztés módszereivel, a műanyagok gépipari alkalmazásával, a gazdaságos gyártás és a korszerű üzemszervezés kérdéseivel."</i>
		</p>
		<p>
			A képzési cél a szakmai tárgyak oktatásán keresztül valósult meg. A képzésben meghatározó szerepe volt a tanterveknek és azon belül a szakmai tananyagoknak. Az évek során a tanterv többször változott és korszerűsödött, azonban valamennyi tantárgyunk oktatásánál az elmélet gyakorlati alkalmazását helyeztük előtérbe. Az elméleti és gyakorlati foglalkozások aránya szakonként változó volt, akkor törekedtünk azonban az átlagos 40% elmélet, 60% gyakorlat arányának tartására. A gépgyártástechnológia szakon a tanszék által oktatott szaktárgyak a főiskolává válás évében az alábbiak voltak:
		</p>
		<ul>
			<li>
				<b>A gépipari mérések</b> című tárgy oktatásának célja a termelés minőségét biztosító mérési módszerek és eszközeik megismertetése. Ennek keretében a hallgatók elsajátították a mérések alapelveit, a mérési hibák jellegét és forrásait, az egyedi, sorozat- és tömeges mérésre gazdaságosan alkalmazható különféle mérőeszközök főbb jellemzőit, a mérőkészülékek elvi felépítését és alkalmazását. <i>(Dr. Szilágyi László)</i>,
			</li>
			<li>
				<b>A forgácsolás elmélete és szerszámai</b> című tárgy foglalkozott a forgácsolás főbb tényezőivel, a forgácsleválasztás alapjelenségeivel, a forgácsolóerőnek és a szerszám éltartamának vizsgálatával, az optimális adatok meghatározásával. A hallgatók megismerték a szerszámok felépítését, élkialakítását, valamint a fontosabb eszközök tervezésének alapjait. <i>(Dr. Angyal Béla, Dr. Dobor Lászlóné)</i>,
			</li>
			<li>
				<b>A forgácsoló megmunkálás</b> tantárgy a különböző forgácsolási módok részletes ismertetését adta az alapváltozatoktól kezdve a különleges eljárásokig. A hallgatók megfelelő jártasságot szereztek a gyártási folyamatok megtervezésében, a műszaki-gazdasági követelményeknek megfelelő megmunkálási módok kiválasztásában és azok körülményeinek meghatározásában. <i>(Dr. Váradi András)</i>,
			</li>
			<li>
